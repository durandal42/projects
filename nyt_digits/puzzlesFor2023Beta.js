function getTodaysPuzzleData() {
  try {
    const nthDayOfTest = getNthDayOfTest()
    const wrappedIndex = nthDayOfTest % puzzlesFor2023Beta.length
    const todaysPuzzle = puzzlesFor2023Beta[wrappedIndex]
    return todaysPuzzle
  } catch (e) {
    // default to first puzzle if anything goes wrong
    captureSentryException(e)
    return puzzlesFor2023Beta[0]
  }
}

const ONE_DAY_EXACTLY_MS = 1000 * 60 * 60 * 24

// starts at 0
function getNthDayOfTest() {
  const startDate = new Date('4/10/2023')
  const todaysDate = new Date()

  const msDiff =
    todaysDate.setHours(0, 0, 0, 0) - startDate.setHours(0, 0, 0, 0)

  var dayDiff = Math.round(msDiff / ONE_DAY_EXACTLY_MS)

  if (dayDiff < 0) {
    dayDiff = 0
  }
  return dayDiff
}

